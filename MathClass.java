public class  MathClass{
	public static void main(String[] args) {
		double power_test = 0;
		power_test = Math.pow(5,2);
		System.out.println(power_test);

		double num1 = 3.5594;
		double num2 = 6.3271;
		double sum;
		sum = num1 + num2;
		System.out.println(sum);

		System.out.println(Math.round(3.27));
		System.out.println(Math.round(3.67));

		System.out.println(Math.min(10,50));
		System.out.println(Math.max(10,50));

		System.out.println(Math.ceil(sum));
		System.out.println(Math.floor(sum));









	}
}
